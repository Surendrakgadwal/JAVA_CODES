public class Maximum_using_switch_case {
    public static void main(String args[]){

        int  num1=11;
        switch(num1%2){

            case 0:
                System.out.println("Num1 is even");
                break;
            case 1:
            System.out.println("Num1 is odd");
            break;



        }

    }



}
