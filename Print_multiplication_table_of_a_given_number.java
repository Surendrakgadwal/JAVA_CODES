public class Print_multiplication_table_of_a_given_number {
    public static void main(String args[]) {
        int num = 5;
        int i = 1;

        while(i<=10)
        {
            System.out.println( num + " * " + i + " = " + (num*i));
            i++;
        }

    }

}
