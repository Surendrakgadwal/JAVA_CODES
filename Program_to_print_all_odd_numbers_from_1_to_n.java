public class Program_to_print_all_odd_numbers_from_1_to_n {

    public static void main(String args[]) {

        int num = 100;
        int i = 1;
        while (i <= num) {

            if (i % 2 == 1)
                System.out.println(i);

            i++;
        }


    }



    }
