public class Program_to_print_sum_of_all_numbers_from_1_to_n {

    public static void main(String args[]) {

        int num = 5;
        int sum=0,i=1;
        while (i <= num) {

            sum=sum+i;
            i++;
        }
        System.out.println(sum);


    }

}
